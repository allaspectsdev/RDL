def generate_report_html(title: str, sections: list) -> str:
    """Generate an HTML report from sections.

    Each section is a dict with:
        - 'title': str
        - 'content': str (HTML) or pd.DataFrame or plotly Figure
    """
    css = """
    <style>
        body { font-family: 'Segoe UI', Tahoma, Geneva, Verdana, sans-serif; margin: 40px;
               color: #1e293b; background: #f8fafc; }
        h1 { color: #6366f1; border-bottom: 3px solid #6366f1; padding-bottom: 10px; }
        h2 { color: #334155; margin-top: 30px; border-bottom: 1px solid #e2e8f0; padding-bottom: 6px; }
        table { border-collapse: collapse; width: 100%; margin: 16px 0; }
        th, td { border: 1px solid #e2e8f0; padding: 8px 12px; text-align: left; }
        th { background: #f1f5f9; font-weight: 600; }
        tr:nth-child(even) { background: #f8fafc; }
        .metric { display: inline-block; background: #fff; border: 1px solid #e2e8f0;
                   border-radius: 8px; padding: 12px 20px; margin: 6px; text-align: center; }
        .metric-value { font-size: 1.4em; font-weight: 700; color: #6366f1; }
        .metric-label { font-size: 0.85em; color: #64748b; }
        .footer { margin-top: 40px; padding-top: 16px; border-top: 1px solid #e2e8f0;
                   color: #94a3b8; font-size: 0.85em; }
        .chart-container { margin: 16px 0; }
    </style>
    """

    now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    html_parts = [
        "<!DOCTYPE html><html><head><meta charset='utf-8'>",
        f"<title>{title}</title>",
        css,
        "<script src='https://cdn.plot.ly/plotly-latest.min.js'></script>",
        "</head><body>",
        f"<h1>{title}</h1>",
        f"<p style='color: #64748b;'>Generated: {now}</p>",
    ]

    for section in sections:
        html_parts.append(f"<h2>{section['title']}</h2>")
        content = section["content"]

        if isinstance(content, pd.DataFrame):
            html_parts.append(content.to_html(index=False, classes="report-table"))
        elif hasattr(content, "to_html"):
            # Plotly figure
            html_parts.append('<div class="chart-container">')
            html_parts.append(content.to_html(full_html=False, include_plotlyjs=False))
            html_parts.append("</div>")
        else:
            html_parts.append(str(content))

        # Append commentary if present
        commentary = section.get("commentary", "")
        if commentary:
            html_parts.append(f"<p><em>{commentary}</em></p>")

    html_parts.append(
        '<div class="footer">Generated by Ryan\'s Data Lab</div>'
    )
    html_parts.append("</body></html>")
    return "\n".join(html_parts)


def render_download_report_button(title: str, sections: list, key: str = "dl_report"):
    """Render a download button for an HTML report."""
    html = generate_report_html(title, sections)
    st.download_button(
        label="Download Report (HTML)",
        data=html,
        file_name=f"{title.lower().replace(' ', '_')}_report.html",
        mime="text/html",
        key=key,
    )


# ─── Content helpers ────────────────────────────────────────────────────────

def _content_preview(content, max_chars=200):
    """Return a short plain-text preview of section content."""
    if isinstance(content, pd.DataFrame):
        preview = f"DataFrame ({content.shape[0]} rows x {content.shape[1]} cols)"
        if len(content) > 0:
            cols_str = ", ".join(content.columns[:5].tolist())
            if len(content.columns) > 5:
                cols_str += f" ... +{len(content.columns) - 5} more"
            preview += f" | Columns: {cols_str}"
        return preview
    elif hasattr(content, "to_image") or hasattr(content, "to_html"):
        # Plotly figure
        title = ""
        if hasattr(content, "layout") and hasattr(content.layout, "title"):
            t = content.layout.title
            if hasattr(t, "text") and t.text:
                title = t.text
        return f"Plotly Figure{': ' + title if title else ''}"
    else:
        text = str(content)
        # Strip HTML tags for preview
        import re
        text = re.sub(r"<[^>]+>", " ", text)
        text = re.sub(r"\s+", " ", text).strip()
        if len(text) > max_chars:
            text = text[:max_chars] + "..."
        return text if text else "(empty)"


def _fig_to_png_bytes(fig, width=900, height=500):
    """Convert a Plotly figure to PNG bytes using kaleido."""
    try:
        return fig.to_image(format="png", width=width, height=height)
    except Exception:
        return None


